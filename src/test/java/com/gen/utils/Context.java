package com.gen.utils;

public class Context {

    private String product;
    private String cataloguePrice;
    private String catalogueDevices;
    private String catalogueYears;

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getCataloguePrice() {
        return cataloguePrice;
    }

    public void setCataloguePrice(String cataloguePrice) {
        this.cataloguePrice = cataloguePrice;
    }

    public String getCatalogueDevices() {
        return catalogueDevices;
    }

    public void setCatalogueDevices(String catalogueDevices) {
        this.catalogueDevices = catalogueDevices;
    }

    public String getCatalogueYears() {
        return catalogueYears;
    }

    public void setCatalogueYears(String catalogueYears) {
        this.catalogueYears = catalogueYears;
    }
}
